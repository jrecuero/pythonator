class Priority(object):
    """ Dependator priorities

    >>> Priority.PRIO_HIGH
    1

    >>> Priority.PRIO_MED
    2

    >>> Priority.PRIO_LOW
    3

    >>> Priority.PRIO_DEFAULT
    2

    >>> Priority.PRIOS
    (1, 2, 3)

    """

    PRIO_HIGH = 1
    """
    :type: int

    High priority
    """

    PRIO_MED  = 2
    """
    :type: int

    Medioum priority
    """

    PRIO_LOW  = 3
    """
    :type: int

    Low priority
    """

    PRIO_DEFAULT = PRIO_MED
    """
    :type: int

    Defaule priority (Medium)
    """

    PRIOS = (PRIO_HIGH, PRIO_MED, PRIO_LOW)
    """
    :type: tuple

    List of priorities
    """


